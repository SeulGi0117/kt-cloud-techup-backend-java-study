package study.seulgi.java.ch01.basics.java;

public class SystemOutMain {
	public static void main(String[] args) {
		// System.out: 표준 출력 장치
		// System.in:	표준 입력 장치
		// System.err: 표준 에러 출력 장치
		// ln 뜻 line new 새로운 줄을 생성한다라는 뜻~

		// 포맷을 지정할 때 형식 지정자
		// %d : 정수
		// %f: 실수
		// %s: 문자열
		// %b: 논리(true, false
		System.out.printf("%d * %d = %d", 10, 20, 10 * 20);

	}
}
